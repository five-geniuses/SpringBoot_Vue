  if (!props.existingProducts || props.existingProducts.length === 0) {
    return 1
  }

  const existingIds = props.existingProducts
    .map(product => product.id)
    .filter(id => id && !isNaN(id))
    .sort((a, b) => a - b)

  let expectedId = 1
  for (const id of existingIds) {
    if (id === expectedId) {
      expectedId++
    } else if (id > expectedId) {
      break
    }
  }

  return expectedId
}

